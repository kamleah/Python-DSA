# Add / Update an element to the dictonary

# Adding an element to a dictionary

# Initialize a dictionary
myDict2 = {"name": "Kamlesh", "age": 27}

# Add a new key-value pair
myDict2["address"] = "Tukaram Nagar, Narpoli, Bhiwandi"

# Print the updated dictionary
print(myDict2)

# Time complexity: O(1)
# Adding a new element to a dictionary takes constant time.
# Space complexity: O(1)
# The additional space required for the new key-value pair is constant.

# Updating an element in a dictionary

# Initialize a dictionary
myDict = {"name": "Kamlesh", "age": 27}

# Update the value of the "age" key
myDict["age"] = 26

# Print the updated dictionary
print(myDict)

# Time complexity: O(1)
# Updating the value associated with a specific key takes constant time.
# Space complexity: O(n)
# The space complexity depends on the number of key-value pairs in the dictionary.
