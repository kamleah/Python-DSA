# Concatenate two lists
a = [1, 2, 3, 4]
b = [5, 6, 7, 8]
c = a + b
print("Concatenated List:", c)

# Using the * operation to repeat elements inside a list
d = [0, 4]
d = d * 4
print("List after Repetition:", d)

# Find the length of a list
e = [0, 1, 2, 3, 4, 5, 6, 7]
print("Length of List:", len(e))

# Find the maximum element of a list
print("Maximum Element:", max(e))

# Find the minimum element of a list
print("Minimum Element:", min(e))

# Calculate the sum of elements in a list
print("Sum of Elements:", sum(e))

# Calculate the average of the elements in the list
average = sum(e) / len(e)
print("Average of Elements:", average)
