# Creating a shopping list
shoppingList = ["Milk", "Cheese", "Butter"]

# Accessing elements using proper indexing
# Indices start from 0 for the first element, 1 for the second, and so on

# Accessing the first element ("Milk") using index 0
first_item = shoppingList[0]
print("First Item:", first_item)

# Accessing the second element ("Cheese") using index 1
second_item = shoppingList[1]
print("Second Item:", second_item)

# Accessing the third element ("Butter") using index 2
third_item = shoppingList[2]
print("Third Item:", third_item)
