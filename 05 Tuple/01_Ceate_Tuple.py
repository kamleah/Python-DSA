# Creating and Working with Tuples

# Creating a tuple with multiple elements
newtuple = ("a", "b", "c", "d", "e", "f")
print("newtuple:", newtuple)  # Output: ('a', 'b', 'c', 'd', 'e', 'f')
print("Type of newtuple:", type(newtuple))  # Output: <class 'tuple'>

# Creating a single-element tuple
singleElementTuple = (
    "a",
)  # Note the comma after "a" to ensure it's treated as a tuple
print("singleElementTuple:", singleElementTuple)  # Output: ('a',)
print(
    "Type of singleElementTuple:", type(singleElementTuple)
)  # Output: <class 'tuple'>
